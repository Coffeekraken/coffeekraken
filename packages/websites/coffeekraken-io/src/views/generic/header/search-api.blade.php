<h3 class="s-typo:h3 s-mbe:50">
    Searching the API
</h3>

<p class="s-typo:lead s-mbe:50">
    To access the search, simply click in the <span class="s-tc:accent">search input on top of the page</span>, of
    make use of the <span class="s-badge s-color:complementary">CTRL + P</span> shortcut...
</p>

<p class="s-typo:p">
    You can make a search through the entire website or scope them using <span
        class="s-badge:outline s-color:accent">/doc ...</span> for the documentation, <span
        class="s-badge:outline s-color:accent">/style ...</span> for the styleguide and <span
        class="s-badge:outline s-color:accent">/api ...</span> for the API.
</p>

<p class="s-typo:p">
    To search for packages, simply use the <span class="s-badge:outline s-color:accent">@...</span> filter pattern.
</p>
